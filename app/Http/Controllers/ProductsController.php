     * @param  string  $slug
     * @return \Illuminate\Http\Response
     */
    public function show($slug)
    {
        $product = Products::where('slug', $slug)->first();
        $likes = Products::where('slug', '!=', $slug)->inRandomOrder()->take(4)->get();

        return view('single-product')->with([
            'title' => $product->name,
            'product' => $product,
            'likes' => $likes,
        ]);
    }

